def main():
    print("日本の重要ニュース2024-2025データ生成開始...")

    results = []
    for example in JAPAN_NEWS_EXAMPLES:
        results.append({
            "messages": [
                {"role": "system", "content": NEWS_SYSTEM},
                {"role": "user", "content": example["user"]},
                {"role": "assistant", "content": example["assistant"]}
            ],
            "metadata": {
                "category": f"news_{example['cat']}",
                "source": "japan_news_2024_2025"
            }
        })

    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        for item in results:
            f.write(json.dumps(item, ensure_ascii=False) + "\n")

    print(f"完了: {len(results)}件 -> {OUTPUT_PATH}")

if __name__ == "__main__":
    main()
